package day2;

public class DataType4 {

	public static void main(String[] args) {
	
		/*
		 *  casting -
		 *  
		 *  upcasting - lower data type to upper data type
		 *  
		 *  downcasting - upper data type to lower data type
		 *  					explicit
		 * 
		 */
		
		byte num1=(byte) 130;
		
		// 128, 127, -128
		
		System.out.println(num1);
		
		
	}

}
