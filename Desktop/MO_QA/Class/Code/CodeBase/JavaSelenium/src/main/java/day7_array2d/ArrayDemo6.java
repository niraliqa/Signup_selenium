package day7_array2d;

public class ArrayDemo6 {

	public static void main(String[] args) {
		
		// 1,4,6,9,2- int
		
		// 11.2, 56.7 ,90.0 - double
		
		// 'a','z','b' - char
		
		// 12,'a',11.5,false,"Mukesh" - Object
		
		Object arr[]= {12,'a',11.5,false,"Mukesh"};
		
		for(Object a:arr)
		{
			System.out.println(a);
		}

	}

}
