package day5;

public class NestedForLoop {

	public static void main(String[] args) 
	{
		for(int i=0;i<3;i=i+1)
		{
			for(int j=0;j<=1;j=j+1)
			{
				System.out.println("I value is "+i);
				
				System.out.println("J Value is "+j);
			}
			
			System.out.println("***********************");
			
		}
	
	}

}
